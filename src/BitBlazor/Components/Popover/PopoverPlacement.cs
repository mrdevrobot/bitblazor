namespace BitBlazor.Components;

/// <summary>
/// Defines the placement options for the <see cref="BitPopover"/> component.
/// </summary>
public enum PopoverPlacement
{
    /// <summary>The popover is placed automatically based on available space.</summary>
    Auto,

    /// <summary>The popover is placed above the trigger element.</summary>
    Top,

    /// <summary>The popover is placed below the trigger element.</summary>
    Bottom,

    /// <summary>The popover is placed to the left of the trigger element.</summary>
    Left,

    /// <summary>The popover is placed to the right of the trigger element.</summary>
    Right
}
