namespace BitBlazor.Components;

/// <summary>
/// Defines the placement options for the <see cref="BitTooltip"/> component.
/// </summary>
public enum TooltipPlacement
{
    /// <summary>The tooltip is placed automatically based on available space.</summary>
    Auto,

    /// <summary>The tooltip is placed above the trigger element.</summary>
    Top,

    /// <summary>The tooltip is placed below the trigger element.</summary>
    Bottom,

    /// <summary>The tooltip is placed to the left of the trigger element.</summary>
    Left,

    /// <summary>The tooltip is placed to the right of the trigger element.</summary>
    Right
}
